"""IEC 60063 preferred-number (E-) series + resistor combination search.

E1/E3/E6/E12/E24 are the **tabulated** historical series from IEC 60063:2015
(they do NOT follow the geometric formula — e.g. E24 has 2.7, 3.0, 3.3 where
the formula gives 2.61, 2.87, 3.16). E48/E96/E192 are formula series:
``round(10^(i/N), 3 significant digits)`` with the single standardized
exception 9.20 (calculated 9.19) in E192.

The combination search mirrors KiCad's resistor calculator behavior: build a
target resistance from up to four standard values (series/parallel), with the
exact requested value and up to two user-excluded values removed from the
pool (component-availability workaround).

References:
* IEC 60063:2015, *Preferred number series for resistors and capacitors*.
* Combination-search behavior cross-checked against KiCad ``pcb_calculator``
  (independent reimplementation; no KiCad code used).
"""

from __future__ import annotations

import bisect
from typing import cast

from .registry import CalcError, Param, Result, register
from .si import fmt_eng

__all__ = ["SERIES", "series_values", "nearest", "snap"]

# --- IEC 60063:2015 tabulated series (must NOT be generated by formula) ---- #
_E1 = (1.0,)
_E3 = (1.0, 2.2, 4.7)
_E6 = (1.0, 1.5, 2.2, 3.3, 4.7, 6.8)
_E12 = (1.0, 1.2, 1.5, 1.8, 2.2, 2.7, 3.3, 3.9, 4.7, 5.6, 6.8, 8.2)
_E24 = (1.0, 1.1, 1.2, 1.3, 1.5, 1.6, 1.8, 2.0, 2.2, 2.4, 2.7, 3.0,
        3.3, 3.6, 3.9, 4.3, 4.7, 5.1, 5.6, 6.2, 6.8, 7.5, 8.2, 9.1)


def _formula(n: int) -> tuple[float, ...]:
    """E48/E96/E192 per IEC 60063: 10^(i/n) to 3 significant digits."""
    vals = []
    for i in range(n):
        v = round(10 ** (i / n), 2)
        # standardized exception: IEC 60063 lists 9.20 where 10^(185/192)
        # rounds to 9.19
        if n == 192 and abs(v - 9.19) < 1e-9:
            v = 9.20
        vals.append(v)
    return tuple(vals)


SERIES: dict[str, tuple[float, ...]] = {
    "E1": _E1, "E3": _E3, "E6": _E6, "E12": _E12, "E24": _E24,
    "E48": _formula(48), "E96": _formula(96), "E192": _formula(192),
}

_SERIES_CHOICES = tuple(SERIES)


def series_values(series: str, lo: float, hi: float) -> list[float]:
    """All ``series`` values in ``[lo, hi]``, spanning decades."""
    base = SERIES[series]
    out = []
    dec = 1e-12
    while dec <= hi:
        for m in base:
            v = m * dec
            if lo <= v <= hi:
                out.append(round(v, 12))
        dec *= 10
    return out


def nearest(value: float, series: str) -> float:
    """Nearest ``series`` value to ``value`` (any decade)."""
    if value <= 0:
        raise CalcError("value must be positive")
    base = SERIES[series]
    best, err = base[0], float("inf")
    import math
    exp = math.floor(math.log10(value))
    for d in (exp - 1, exp, exp + 1):
        for m in base:
            v = m * 10 ** d
            e = abs(v - value)
            if e < err:
                best, err = v, e
    return round(best, 12)


def snap(value: float, series: str) -> tuple[float, float]:
    """(nearest value, signed error %)."""
    v = nearest(value, series)
    return v, (v - value) / value * 100.0


